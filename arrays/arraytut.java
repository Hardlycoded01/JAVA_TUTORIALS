public class arraytut {
    public static void main(String[] args) {
        int []mat={1,2,3,4,5};
        System.out.println("Array:");
        System.out.print("[ ");
        for(int i=0;i<mat.length;i++){
            System.out.print(mat[i]);
            System.out.print(" ");
        }   
        System.out.println("]");
    }
}
